public class Box1 {
   // PRIVATE VAR
   private int x, y;
   // CONSTRUCTOR
   public Box1(int x, int y)
   {
     this.x = x;
     this.y = y;
   }

   // ACCESSOR
   public int getX()
   {
      return this.x;
   }
   public int getY()
   {
      return this.y;
   }

   // MUTATOR
   public void setX(int x)
   {
      this.x = x;
   }
   public void setY(int y)
   {
      this.y = y;
   }
}
